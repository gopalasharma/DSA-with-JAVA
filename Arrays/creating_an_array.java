package Arrays;

public class creating_an_array {
    public static void main(String[] args) {
        
        // define an array.
        
        int marks[]= new int[50]; // created an array of size 50.

        // if we know what to store then directly store
        // the value in the array. There is no need to define
        // the size of an array. Java automatically
        // give the size to that array.
        
        int numbers[]= {1,2,3}; //java gives the size automatically 3.

        // more arrays are:

        int moreNumbers[]= {4,5,6};

        // string type array

        String fruits[]= {"apple", "mango", "banana"};

    }
}
