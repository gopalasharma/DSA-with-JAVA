package Basic_of_Java;

public class relational_operator {
    public static void main(String args[]){
        int a= 10;
        int b= 5;
        System.out.println(a == b); /*To check the equality that the numbers\
        are equal or not if it is equal then print true otherwise false */
        System.out.println(a!=b); /* To check the inequality that
        the numbers are not equal if it is equal print false otherwise true */
        System.out.println(a > b); /* To check a is greater then b or not */
        System.out.println(a < b); /* To check a is smaller then b or not */
        System.out.println(a >= b); /* To check a is greater or equal to b */
        System.out.println(a <= b); /* To check a is smaller or equal to b */

    }

}
