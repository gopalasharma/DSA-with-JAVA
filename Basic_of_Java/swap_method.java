package Basic_of_Java;

public class swap_method {
    public static void main(String[] args) {
   
        int a= 5;
        int b= 6;

        //swap_function

        int temp= a;
        a= b;
        b= temp;
        System.out.println("a is = " + a);
        System.out.println("b is = " + b);
    }
}

